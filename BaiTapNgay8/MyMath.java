package BaiTapNgay8;

public class MyMath {
    public static final double PI = 3.14;

    public static double sum(int a) {
        return a + PI;
    }

    public static int min(int a, int b) {
        return a < b ? a : b;
    }

    public static int sum(int a, int b) {
        return a + b;
    }
}

